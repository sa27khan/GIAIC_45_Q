let num1 = Number(prompt('Enter first number: '))
let num2 = Number(prompt('Enter second number: '))
let op = prompt('Enter operator: ')
// created by Najaf Ali
if (op=='+'){
    console.log(num1+num2)
}
if (op=='-'){
    console.log(num1-num2)
}
if (op=='*'){
    console.log(num1*num2)
}
if (op=='/'){
    console.log(num1/num2)
}
